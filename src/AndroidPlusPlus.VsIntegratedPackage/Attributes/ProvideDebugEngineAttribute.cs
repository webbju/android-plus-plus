    public string [] IncompatibleList
    {
      get
      {
        return null;
      }
      set
      {
        foreach (string engineId in value)
        {
          Guid incompatibleEngineGuid = new Guid (engineId);

          string debugEngineName = VsDebugEngine.DebugEngineGuids.GetEngineNameFromId (incompatibleEngineGuid);

          m_engineIncompatibleIds [debugEngineName] = incompatibleEngineGuid;
        }
      }
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public string [] PortSupplier
    {
      get
      {
        return null;
      }
      set
      {
        foreach (string supplierId in value)
        {
          Guid incompatibleSupplierGuid = new Guid (supplierId);

          string debugEngineName = m_enginePortSupplierIds.Count.ToString ();

          m_enginePortSupplierIds [debugEngineName] = incompatibleSupplierGuid;
        }
      }
    }

