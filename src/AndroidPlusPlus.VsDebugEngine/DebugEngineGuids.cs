    public static string GetEngineNameFromId (Guid engineGuid)
    {
      LoggingUtils.PrintFunction ();

      switch (engineGuid.ToString ().ToUpper ())
      {
        case guidDebugEngineStringID:
          return "Android++";

        case guidIncompatibleDebugEngineSilverlightStringID:
          return "guidSilverlightEng";

        case guidIncompatibleDebugEngineTSql2000StringID:
          return "guidSQLEng2";

        case guidIncompatibleDebugEngineTSql2005StringID:
          return "guidSQLEng2";

        case guidIncompatibleDebugEngineNativeStringID:
          return "guidNativeOnlyEng";

        case guidIncompatibleDebugEngineManagedStringID:
          return "guidCOMPlusOnlyEng";

        case guidIncompatibleDebugEngineManaged20StringID:
          return "guidCOMPlusOnlyEng2";

        case guidIncompatibleDebugEngineManaged40StringID:
          return "guidCOMPlusOnlyEng4";

        case guidIncompatibleDebugEngineWorkflowStringID:
          return "guidIncompatibleDebugEngineWorkflow";

        case guidIncompatibleDebugEngineManagedAndNativeStringID:
          return "guidCOMPlusNativeEng";

        case guidIncompatibleDebugEngineScriptStringID:
          return "guidScriptEng";
      }

      return engineGuid.ToString ();
    }


