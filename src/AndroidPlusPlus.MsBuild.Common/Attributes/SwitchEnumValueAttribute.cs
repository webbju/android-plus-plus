using Microsoft.Build.Framework.XamlTypes;
using System;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

namespace AndroidPlusPlus.MsBuild.Common.Attributes
{

  [AttributeUsage(AttributeTargets.Property, AllowMultiple = true)]
  public class SwitchEnumValueAttribute : Attribute
  {

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public SwitchEnumValueAttribute()
    {
      enumValue = new EnumValue();
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    protected readonly EnumValue enumValue;

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public EnumValue EnumValue
    {
      get => enumValue;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    // The name of this Microsoft.Build.Framework.XamlTypes.EnumValue.
    public string Name
    {
      get => enumValue.Name;
      set => enumValue.Name = value;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    // The name that could be used by a prospective UI client to display this Microsoft.Build.Framework.XamlTypes.EnumValue.
    public string DisplayName
    {
      get => enumValue.DisplayName;
      set => enumValue.DisplayName = value;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    // Description of this Microsoft.Build.Framework.XamlTypes.BaseProperty for use by a prospective UI client.
    public string Description
    {
      get => enumValue.Description;
      set => enumValue.Description = value;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    // Help information for this Microsoft.Build.Framework.XamlTypes.EnumValue.
    public string HelpString
    {
      get => enumValue.HelpString;
      set => enumValue.HelpString = value;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    // The switch representation of this property for the case when the parent Microsoft.Build.Framework.XamlTypes.EnumProperty represents a tool parameter.
    public string Switch
    {
      get => enumValue.Switch;
      set => enumValue.Switch = value;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    // The prefix for the switch representation of this value for the case when the parent Microsoft.Build.Framework.XamlTypes.EnumProperty represents a tool parameter.
    public string SwitchPrefix
    {
      get => enumValue.SwitchPrefix;
      set => enumValue.SwitchPrefix = value;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    // Tells if this Microsoft.Build.Framework.XamlTypes.EnumValue is the default value for the associated Microsoft.Build.Framework.XamlTypes.EnumProperty.
    public bool IsDefault
    {
      get => enumValue.IsDefault;
      set => enumValue.IsDefault = value;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  }
}
