////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

using Microsoft.Build.Framework.XamlTypes;
using System;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

namespace AndroidPlusPlus.MsBuild.Common.Attributes
{

  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  [AttributeUsage(AttributeTargets.Property, AllowMultiple = false)]
  public class SwitchDataSourceAttribute : Attribute
  {

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public SwitchDataSourceAttribute()
    {
      dataSource = new DataSource();
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    protected readonly DataSource dataSource;

    public DataSource DataSource
    {
      get => dataSource;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    // The storage location for this data source.
    public string Persistence
    {
      get => dataSource.Persistence;
      set => dataSource.Persistence = value;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    // The storage style for this data source.
    public string PersistenceStyle
    {
      get => dataSource.PersistenceStyle;
      set => dataSource.PersistenceStyle = value;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    // Gets or sets the actual MSBuild property name used to read/write the value of this property.
    // Applicable only to Microsoft.Build.Framework.XamlTypes.DataSource objects attached to properties.
    public string PersistedName
    {
      get => dataSource.PersistedName;
      set => dataSource.PersistedName = value;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    // The label of the MSBuild property group/item definition group to which a property/item definition metadata belongs to.
    // Default value is the empty string.
    public string Label
    {
      get => dataSource.Label;
      set => dataSource.Label = value;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    // If a Microsoft.Build.Framework.XamlTypes.BaseProperty is an item definition metadata or item metadata,
    // this field specified the item type of the item definition or the item, respectively.
    // For common properties this field must not be set.
    public string ItemType
    {
      get => dataSource.ItemType;
      set => dataSource.ItemType = value;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    // Indicates if a property is configuration-dependent as indicated by the presence of a configuration condition attached to
    // the property definition at its persistence location.
    public bool HasConfigurationCondition
    {
      get => dataSource.HasConfigurationCondition;
      set => dataSource.HasConfigurationCondition = value;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    // The data type of the source. Generally one of Item, ItemDefinition, Property, or TargetResults
    // (when Microsoft.Build.Framework.XamlTypes.DataSource.MSBuildTarget is non-empty).
    // Among other things this governs how the data is treated during build.
    public string SourceType
    {
      get => dataSource.SourceType;
      set => dataSource.SourceType = value;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    // Gets or sets the semicolon-delimited list of MSBuild targets that must be executed before reading the read-only properties
    // or items described by this Microsoft.Build.Framework.XamlTypes.DataSource.
    public string MSBuildTarget
    {
      get => dataSource.MSBuildTarget;
      set => dataSource.MSBuildTarget = value;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    // Gets or sets a value indicating where the default value for this property can be found.
    public DefaultValueSourceLocation SourceOfDefaultValue
    {
      get => dataSource.SourceOfDefaultValue;
      set => dataSource.SourceOfDefaultValue = value;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  }
}
